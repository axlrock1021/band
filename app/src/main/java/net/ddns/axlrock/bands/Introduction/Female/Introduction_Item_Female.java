package net.ddns.axlrock.bands.Introduction.Female;

public class Introduction_Item_Female {

    private int id;
    private int image2;
    private String name;

    public Introduction_Item_Female() {
        super();
    }

    public Introduction_Item_Female(int id, int image2, String name) {
        super();
        this.id = id;
        this.image2 = image2;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getImage2() {
        return image2;
    }

    public void setImage2(int image2) {
        this.image2 = image2;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
